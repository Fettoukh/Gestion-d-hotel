    private void btnajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajouterActionPerformed

        String cin=this.txtcin.getText();
          if(cin.length()>11)
         {
             JOptionPane.showMessageDialog(null, " CIN est invalide !!");
             txtcin.setText("");
               return; 
             
         }
        String nom =this.txtnom.getText();
        String prenom=this.txtprenom.getText();
        String telephone = this.txttelephone.getText();
        String email=this.txtemail.getText();
        
        Date datenaissance = this.jdatenaissance.getDate();
        Calendar cal1 = Calendar.getInstance();
           
      
            cal1.set(Calendar.YEAR+1900-18, 1, 1);
          
            Date DateNaissanceMini=new java.sql.Date(cal1.getTime().getTime());
         if(datenaissance.after(DateNaissanceMini))
        {
           
              JOptionPane.showMessageDialog(null, "Date naissance invalide!!");
              return;
        }
        String Sexe=this.cbxsexe.getSelectedItem().toString();
        String adresse=this.txtadresse.getText();
        int codepostal=Integer.parseInt(this.txtcodepostal.getText());
        

        /* contrat */
        Date datedebut=this.jdatedebut.getDate();
        Date datefin=this.jdatefin.getDate();
        Date date= new java.sql.Date(Calendar.getInstance().getTime().getTime());
         String role=this.txtrole.getText();
        float salaire=Float.parseFloat(this.txtsalaire.getText());
        String typec=this.cbcontrat.getSelectedItem().toString();
        String description = this.txtdescription.getText();
        String login = cin+"_"+prenom;
 
         int temp =(int)( Math.random()*( 999999 - 100000 + 1 ) ) + 100000;
         String password = ""+temp  ;
         
         if(this.jdir.dr.gp.CheckCinPersonnel(cin))
          {
               JOptionPane.showMessageDialog(null, "un employe possedant ce CIN existe deja !!");
               return; 
          }
         int reponse =JOptionPane.showConfirmDialog(null, "Etes vous sur du vouloir ajouter ses Employer", "Fermeture !!!", JOptionPane.YES_NO_OPTION);
         if(reponse==JOptionPane.NO_OPTION)
        {
            return;
        }
          
        if(typec.equalsIgnoreCase("CDI"))
        {
            Calendar cal = Calendar.getInstance();
           
      
            cal.set(2099, 1, 1);
          
            datefin=new java.sql.Date(cal.getTime().getTime());
             
        }
        if(datefin.before(datedebut) || datedebut.before(date) ){

            JOptionPane.showMessageDialog(null, " la date fin ou la date debut non valide ");
            return ;

        }

        if(role.equalsIgnoreCase("gerant"))
        {
            this.jdir.dr.gp.ajouterGerant(cin, nom, prenom, datenaissance, email, telephone, Sexe, adresse,codepostal);
            this.jdir.dr.gcp.ajouter_Compte(login, cin, password, role);

        }
        else if(role.equalsIgnoreCase("receptionniste"))
        {
            this.jdir.dr.gp.ajouterReceptioniste(cin, nom, prenom, datenaissance, email, telephone, Sexe, adresse, codepostal);
            this.jdir.dr.gcp.ajouter_Compte(login, cin, password, role);

        }
        else if(role.equalsIgnoreCase("autres"))
        {
            this.jdir.dr.gp.ajouterEmploye(cin, nom, prenom, datenaissance, email, telephone, Sexe, adresse, codepostal);
            this.jdir.dr.gcp.ajouter_Compte(login, cin, password, role);
            this.jdir.dr.gcp.Desactiver_Compte(cin);

        }

        this.jdir.dr.gc.AjouteContrat(cin, this.jdir.dr.getCinE(), datefin, datedebut, typec, role, salaire, description);
        JOptionPane.showMessageDialog(null, "Personnel Ajoutée !!");
        vider();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnajouterActionPerformed

