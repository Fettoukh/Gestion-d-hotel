   /**
    * cette fonction permet d'afficher les employes dans le tableau
    * selon le choix de l'utilisateur 
    */
    public void  choixtable()
    {
          if(this.cbempprecedents.isSelected() && this.cbempactuel.isSelected()) 
        {
          ResultSet rs =this.jdir.dr.gp.getAllEmploye();
           this.tableemploye.setModel(new MonModele(rs));
        }
        
        else if( this.cbempprecedents.isSelected() && !this.cbempactuel.isSelected())
        {
            ResultSet rs =this.jdir.dr.gp.getAllEmployeprecedents();
           this.tableemploye.setModel(new MonModele(rs));
            
        }
        else if(this.cbempactuel.isSelected() &&  !this.cbempprecedents.isSelected()  )
        { 
            ResultSet rs =this.jdir.dr.gp.getAllEmployeactuels();
            this.tableemploye.setModel(new MonModele(rs));
        }
        else  
        {
            ResultSet rs =this.jdir.dr.gp.getAllEmployeactuels();
            this.tableemploye.setModel(new MonModele(rs));
            this.cbempactuel.setSelected(true);
        }
    }

