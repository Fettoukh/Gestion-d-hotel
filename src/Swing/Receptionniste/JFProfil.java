    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            ResultSet Res = Gcp.get_Compte(cin);
            Res.first();
            if(Res.getString(2).equalsIgnoreCase(txtmdp.getText()))
            {
                lblInfo.setForeground(Color.RED);
                lblInfo.setText("Le nouveau et l'ancien MDPs sont indentiques !!");
            }
            else
            {
                Gcp.modifier_mdp(lbLogin.getText(), txtmdp.getText());
                lblInfo.setForeground(Color.GREEN);
                lblInfo.setText("Mot de passe Modifi√© !!");
            }
   
        } catch (SQLException ex) {
            Logger.getLogger(JFProfil.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

