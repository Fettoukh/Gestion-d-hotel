    public JFProfil(String cin) {
        initComponents();
        txtmdp.setText("");
        lblInfo.setText("");
        this.cin = cin ;
        try {
            ResultSet Res = Gr.Information_Receptionniste(cin);
            Res.first();
            lblCin.setText(cin);
            lblnom.setText(Res.getString(2));
            lblprenom.setText(Res.getString(3));
            lblTel.setText(Res.getString(6));
            lblEmail.setText(Res.getString(5));
            lblDatedN.setText(Res.getDate(4).toString());
            lblAdresse.setText(Res.getString(8));
            Res = Gcp.get_Compte(cin);
            Res.first();
            lbLogin.setText(Res.getString(1));
            txtmdp.setText(Res.getString(2));
        } catch (SQLException ex) {
            Logger.getLogger(JFProfil.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }

