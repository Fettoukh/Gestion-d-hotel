   public void FiltrerNom()
    {
        txtRechercher.getDocument().addDocumentListener(new DocumentListener() {
            public void changedUpdate(DocumentEvent e) {
                warn();
            }

            public void removeUpdate(DocumentEvent e) {
                warn();
            }

            public void insertUpdate(DocumentEvent e) {
                warn();
            }

            public void warn() {
              if(boxRecherche.getSelectedItem()=="Nom")
              {
                  if(RadioReserver.isSelected()==false)
                  {
                      if(txtRechercher.getText()== null)
                      {
                          AfficherAllClient();
                      }
                      else 
                      {   
                          ResultSet res = Gc.FiltrerNom(txtRechercher.getText());
                          TClients.setModel(new MonModele(res));
                      } 
                  }
                  else
                  {
                      if(txtRechercher.getText()== null)
                      {
                          AfficherReservation();
                      }
                      else
                      {   
                          ResultSet res = Gc.Client_Actuel_Filtrer_Nom(txtRechercher.getText());
                          TClients.setModel(new MonModele(res));
                      }
                  }
              }
              else
              {
                  if(RadioReserver.isSelected()==false)
                  {
                      if(txtRechercher.getText()== null)
                      {
                          AfficherAllClient();
                      }
                      else
                      {   
                          ResultSet res = Gc.FiltrerCin(txtRechercher.getText());
                          TClients.setModel(new MonModele(res));
                      } 
                  }  
                  else
                  {
                      if(txtRechercher.getText()== null)
                      {
                          AfficherReservation();
                      }
                      else
                      {   
                          ResultSet res = Gc.Client_Actuel_Filtrer_Cin(txtRechercher.getText());
                          TClients.setModel(new MonModele(res));
                      }
                  }
              }
            }
        });
    }

