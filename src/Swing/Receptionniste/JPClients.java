    private void TClientsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TClientsMouseClicked
        if (RadioReserver.isSelected() == false )
        {
        if (evt.getClickCount() == 2) {
            int lig = TClients.getSelectedRow();
            new JFModifierClients(Jp,TClients.getValueAt(lig, 0).toString()).setVisible(true);
            txtRechercher.setText("");
        }  
        else
        {
            btnSupprimer.setEnabled(true);
        }
        }
        
    }//GEN-LAST:event_TClientsMouseClicked

