    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        // TODO add your handling code here:
        int reponse =JOptionPane.showConfirmDialog(null, "Etes vous sur de vouloir Supprimer le Client !!", "Suppression !!!", JOptionPane.YES_NO_OPTION);
        if(reponse==JOptionPane.YES_OPTION)
        {
            int lig = TClients.getSelectedRow();
            Gc.delete_client(TClients.getValueAt(lig, 2).toString());
            AfficherAllClient();
            txtRechercher.setText("");
        }
    }//GEN-LAST:event_btnSupprimerActionPerformed

    
